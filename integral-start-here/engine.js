function navTree(){
  let html = "", lastGrp = null;
  STEPS.forEach((s, idx)=>{
    if(s.group !== lastGrp){ html += '<div class="grp">'+s.group+'</div>'; lastGrp = s.group; }
    const cls = (idx===i?"on":"") + (visited.has(idx)&&idx!==i?" done":"");
    const dot = visited.has(idx) ? (idx===i?"▸":"✓") : "·";
    html += '<button class="'+cls+'" onclick="go('+idx+')"><span class="dot">'+dot+'</span>'+s.nav+'</button>';
  });
  navEl.innerHTML = html;
}

function fieldRows(o){
  return o.fields.map(f=>{
    const tags = (f.t||[]).map(tag).join(" ");
    const st = f.s==="new" ? '<span class="tag t-new">new</span>' : f.s==="updated" ? '<span class="tag t-upd">updated</span>' : "";
    const cls = f.s==="new" ? "new" : f.s==="updated" ? "upd" : "";
    const ty = f.ty ? `<span class="ftype">${f.ty}</span>` : "";
    const tail = (tags || st) ? ` <span class="ftags">${tags}${tags && st ? " " : ""}${st}</span>` : "";
    return `<div class="field ${cls}"><span class="fn"><code>${f.fn}</code>${ty}</span><span class="fnote">${f.note}${tail}</span></div>`;
  }).join("");
}

