function esc(t){return t.replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;");}

function render(){
  const s = STEPS[i];
  content.innerHTML = s.kind==="module" ? renderModule(s)
    : '<span class="badge">'+s.badge+'</span><h2>'+s.title+'</h2>'+s.body;
  content.parentElement.scrollTop = 0;
  document.getElementById("stepnum").textContent = i+1;
  document.getElementById("steptot").textContent = STEPS.length;
  document.getElementById("pbar").style.width = ((i+1)/STEPS.length*100)+"%";
  document.getElementById("back").disabled = i===0;
  document.getElementById("next").disabled = i===STEPS.length-1;
  navTree();
  renderPanel();
}

// ---- persistent "data so far" panel ----
function currentModule(){ // highest module number reached by current step
  let m = 0;
  for(let k=0;k<=i;k++){ if(STEPS[k].kind==="module") m = Math.max(m, STEPS[k].mod); }
  return m;
}
function renderPanel(){
  const m = currentModule();
  const issuecard = document.getElementById("issuecard");
  issuecard.style.display = m>=1 ? "block" : "none";
  // status track: status index = module number-1 (M1→intake … M8→dispatched)
  const curStatus = m>=1 ? Math.min(m,STATUS.length)-1 : -1;
  document.getElementById("statustrack").innerHTML = STATUS.map((st,idx)=>{
    const c = idx<curStatus?"done":idx===curStatus?"cur":"";
    return '<span class="stchip '+c+'">'+st+'</span>';
  }).join("");
  // objects
  document.getElementById("objlist").innerHTML = OBJECTS.map(o=>{
    const live = m>=o.at, fresh = o.at===m;
    const cls = "objrow"+(live?" live":"")+(fresh?" fresh":"");
    return '<div class="'+cls+'"><span class="dotc '+o.tier+'"></span>'+o.type+
           '<span class="ocount">×'+o.count+(fresh?' · new':'')+'</span></div>';
  }).join("");
}

